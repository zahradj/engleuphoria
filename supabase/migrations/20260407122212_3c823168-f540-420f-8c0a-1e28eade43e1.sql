CREATE OR REPLACE FUNCTION public.enqueue_email(_queue_name TEXT, _payload JSONB)
RETURNS BIGINT
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  ttl_minutes INTEGER;
  new_id BIGINT;
BEGIN
  SELECT CASE
    WHEN _queue_name = 'auth_emails' THEN auth_email_ttl_minutes
    ELSE transactional_email_ttl_minutes
  END
  INTO ttl_minutes
  FROM public.email_send_state
  WHERE id = 1;

  INSERT INTO public.email_queue_messages (
    queue_name,
    payload,
    expires_at
  )
  VALUES (
    _queue_name,
    _payload,
    now() + make_interval(mins => COALESCE(ttl_minutes, 60))
  )
  RETURNING id INTO new_id;

  RETURN new_id;
END;
$$;

CREATE OR REPLACE FUNCTION public.read_email_batch(_queue_name TEXT, _batch_size INTEGER DEFAULT NULL)
RETURNS TABLE (
  id BIGINT,
  payload JSONB,
  attempts INTEGER,
  expires_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  RETURN QUERY
  WITH picked AS (
    SELECT q.id
    FROM public.email_queue_messages q
    WHERE q.queue_name = _queue_name
      AND q.visible_at <= now()
    ORDER BY q.created_at ASC
    LIMIT COALESCE(
      _batch_size,
      (SELECT batch_size FROM public.email_send_state WHERE id = 1),
      10
    )
    FOR UPDATE SKIP LOCKED
  ),
  claimed AS (
    UPDATE public.email_queue_messages q
    SET attempts = q.attempts + 1,
        claimed_at = now(),
        visible_at = now() + interval '30 seconds'
    FROM picked
    WHERE q.id = picked.id
    RETURNING q.id, q.payload, q.attempts, q.expires_at, q.created_at
  )
  SELECT claimed.id, claimed.payload, claimed.attempts, claimed.expires_at, claimed.created_at
  FROM claimed;
END;
$$;

CREATE OR REPLACE FUNCTION public.delete_email(_queue_name TEXT, _message_id BIGINT)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  deleted_count INTEGER;
BEGIN
  DELETE FROM public.email_queue_messages
  WHERE id = _message_id
    AND queue_name = _queue_name;

  GET DIAGNOSTICS deleted_count = ROW_COUNT;
  RETURN deleted_count > 0;
END;
$$;

CREATE OR REPLACE FUNCTION public.move_to_dlq(_queue_name TEXT, _message_id BIGINT, _error_message TEXT DEFAULT NULL)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  moved_count INTEGER;
BEGIN
  WITH moved AS (
    DELETE FROM public.email_queue_messages
    WHERE id = _message_id
      AND queue_name = _queue_name
    RETURNING id, queue_name, payload, attempts
  )
  INSERT INTO public.email_queue_dlq (
    original_message_id,
    queue_name,
    payload,
    attempts,
    error_message
  )
  SELECT id, queue_name, payload, attempts, _error_message
  FROM moved;

  GET DIAGNOSTICS moved_count = ROW_COUNT;
  RETURN moved_count > 0;
END;
$$;